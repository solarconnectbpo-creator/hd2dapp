/**
 * One-time / dev: regenerates src/roofReports/lowSlopePricingCatalog.generated.ts from data/lowSlopePricing/*.csv
 * Run: node scripts/parse-low-slope-csv.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import Papa from "papaparse";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const dataDir = path.join(root, "data", "lowSlopePricing");
const outFile = path.join(root, "src", "roofReports", "lowSlopePricingCatalog.generated.ts");

const FILES = [
  ["modified-bitumen", "modified-bitumen.csv"],
  ["epdm", "epdm.csv"],
  ["tpo", "tpo.csv"],
  ["pvc", "pvc.csv"],
  ["roof-coatings", "roof-coatings.csv"],
];

function money(s) {
  if (s == null || String(s).trim() === "") return undefined;
  const t = String(s).replace(/[$,]/g, "").trim();
  if (t === "" || t === "—") return undefined;
  const n = parseFloat(t);
  return Number.isFinite(n) ? n : undefined;
}

function parseSheet(systemKey, filename) {
  const filePath = path.join(dataDir, filename);
  const content = fs.readFileSync(filePath, "utf8");
  const parsed = Papa.parse(content, { skipEmptyLines: true });
  const rows = parsed.data;
  let start = 0;
  for (let i = 0; i < rows.length; i++) {
    const c0 = String(rows[i][0] ?? "").trim();
    if (c0 === "DESCRIPTION") {
      start = i + 1;
      break;
    }
  }

  const items = [];
  let section = "";

  for (let i = start; i < rows.length; i++) {
    const row = rows[i];
    const desc = String(row[0] ?? "").trim();
    if (!desc) continue;
    if (/^Subtotal/i.test(desc)) continue;
    if (/^ROOFING SYSTEM/i.test(desc)) continue;
    if (/^Insured:/i.test(desc)) continue;
    if (/^Price List:/i.test(desc)) continue;
    if (/^Type of Loss:/i.test(desc)) continue;
    if (/^Claim #/i.test(desc)) continue;
    if (/^Mil options:/i.test(desc)) continue;
    if (/^APP = /i.test(desc)) continue;
    if (/^Enter QTY/i.test(desc)) continue;

    const unit = String(row[2] ?? "").trim().toUpperCase();
    const qtyRaw = row[1];

    if (!["SQ", "SF", "LF", "EA", "HR"].includes(unit)) {
      if (desc.length > 12 && !String(qtyRaw).match(/^\d/)) {
        section = desc;
      }
      continue;
    }

    items.push({
      system: systemKey,
      section: section || undefined,
      description: desc,
      unit,
      qtyTemplate: String(qtyRaw ?? "").trim(),
      removePerUnitUsd: money(row[3]),
      replacePerUnitUsd: money(row[4]),
      taxPerUnitUsd: money(row[5]),
      notes: String(row[8] ?? row[7] ?? "").trim() || undefined,
    });
  }

  return items;
}

const PRICE_LIST_REF = "MOSL8X_OCT25 (AI cheat sheet export)";

const all = [];
for (const [key, file] of FILES) {
  all.push(...parseSheet(key, file));
}

const header = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/parse-low-slope-csv.mjs — do not edit by hand.
 * Source: data/lowSlopePricing/*.csv (MOSL8X_OCT25 price list)
 */
export const LOW_SLOPE_PRICE_LIST_REFERENCE = ${JSON.stringify(PRICE_LIST_REF)};

export type LowSlopeCatalogSystem =
  | "modified-bitumen"
  | "epdm"
  | "tpo"
  | "pvc"
  | "roof-coatings";

export interface LowSlopePricingLineItem {
  system: LowSlopeCatalogSystem;
  section?: string;
  description: string;
  unit: string;
  qtyTemplate: string;
  removePerUnitUsd?: number;
  replacePerUnitUsd?: number;
  taxPerUnitUsd?: number;
  notes?: string;
}

export const LOW_SLOPE_PRICING_LINES: LowSlopePricingLineItem[] = ${JSON.stringify(all, null, 0)};
`;

fs.writeFileSync(outFile, header, "utf8");
console.log("Wrote", outFile, "lines:", all.length);
